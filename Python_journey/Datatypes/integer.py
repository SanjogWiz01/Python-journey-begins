# integers_floats.py
# Demonstrates integer, float, and basic arithmetic operations

a = 10
b = 3.5

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Power:", a ** 2)
print("Type of a:", type(a))
print("Type of b:", type(b))
